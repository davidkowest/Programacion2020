package pruebaobjetos;

public class Punto {
	//Atributos
	
	private float x;
	private float y;

	//Constructor
	Punto (float x,float y) {
		this.x=x;
		this.y=y;
	}
	
	//Metodos
	public float getX() {
		return x;
	}
	public void setX(float x) {
		this.x = x;
	}
	public float getY() {
		return y;
	}
	public void setY(float y) {
		this.y = y;
	}
	
	
}
